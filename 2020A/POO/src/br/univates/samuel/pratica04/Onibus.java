package br.univates.samuel.pratica04;

public class Onibus {
	public String fabricanteMotor;
	public String montadorCarroceria;
	public double potenciaMotorCV;
	public double velocidadeMaxima;
	public String tipoCombustivel;
	public double capacidadeTanque;
	public int quantAssentos;
	public int passageirosEmPe;
	public boolean arCondicionado;
	
	
	public String getFabricanteMotor() {
		return fabricanteMotor;
	}
	public void setFabricanteMotor(String fabricanteMotor) {
		this.fabricanteMotor = fabricanteMotor;
	}
	public String getMontadorCarroceria() {
		return montadorCarroceria;
	}
	public void setMontadorCarroceria(String montadorCarroceria) {
		this.montadorCarroceria = montadorCarroceria;
	}
	public double getPotenciaMotorCV() {
		return potenciaMotorCV;
	}
	public void setPotenciaMotorCV(double potenciaMotorCV) {
		this.potenciaMotorCV = potenciaMotorCV;
	}
	public double getVelocidadeMaxima() {
		return velocidadeMaxima;
	}
	public void setVelocidadeMaxima(double velocidadeMaxima) {
		this.velocidadeMaxima = velocidadeMaxima;
	}
	public String getTipoCombustivel() {
		return tipoCombustivel;
	}
	public void setTipoCombustivel(String tipoCombustivel) {
		this.tipoCombustivel = tipoCombustivel;
	}
	public double getCapacidadeTanque() {
		return capacidadeTanque;
	}
	public void setCapacidadeTanque(double capacidadeTanque) {
		this.capacidadeTanque = capacidadeTanque;
	}
	public int getQuantAssentos() {
		return quantAssentos;
	}
	public void setQuantAssentos(int quantAssentos) {
		this.quantAssentos = quantAssentos;
	}
	public int getPassageirosEmPe() {
		return passageirosEmPe;
	}
	public void setPassageirosEmPe(int passageirosEmPe) {
		this.passageirosEmPe = passageirosEmPe;
	}
	public boolean isArCondicionado() {
		return arCondicionado;
	}
	public void setArCondicionado(boolean arCondicionado) {
		this.arCondicionado = arCondicionado;
	}
	
	

}
