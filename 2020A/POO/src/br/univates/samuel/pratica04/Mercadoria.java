package br.univates.samuel.pratica04;

public class Mercadoria {
	public String nome;
	public int codigoProduto;
	public String codigoAlfaNumerico;
	public long codigoDeBarras;
	public String grupoProduto;
	public String subGrupoProduto;
	public double precoCusto;
	public double precoDeVenda;
	public int quantidadeUnitario;
	public String marcaFabricante;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getCodigoProduto() {
		return codigoProduto;
	}
	public void setCodigoProduto(int codigoProduto) {
		this.codigoProduto = codigoProduto;
	}
	public String getCodigoAlfaNumerico() {
		return codigoAlfaNumerico;
	}
	public void setCodigoAlfaNumerico(String codigoAlfaNumerico) {
		this.codigoAlfaNumerico = codigoAlfaNumerico;
	}
	public long getCodigoDeBarras() {
		return codigoDeBarras;
	}
	public void setCodigoDeBarras(long codigoDeBarras) {
		this.codigoDeBarras = codigoDeBarras;
	}
	public String getGrupoProduto() {
		return grupoProduto;
	}
	public void setGrupoProduto(String grupoProduto) {
		this.grupoProduto = grupoProduto;
	}
	public String getSubGrupoProduto() {
		return subGrupoProduto;
	}
	public void setSubGrupoProduto(String subGrupoProduto) {
		this.subGrupoProduto = subGrupoProduto;
	}
	public double getPrecoCusto() {
		return precoCusto;
	}
	public void setPrecoCusto(double precoCusto) {
		this.precoCusto = precoCusto;
	}
	public double getPrecoDeVenda() {
		return precoDeVenda;
	}
	public void setPrecoDeVenda(double precoDeVenda) {
		this.precoDeVenda = precoDeVenda;
	}
	public int getQuantidadeUnitario() {
		return quantidadeUnitario;
	}
	public void setQuantidadeUnitario(int quantidadeUnitario) {
		this.quantidadeUnitario = quantidadeUnitario;
	}
	public String getMarcaFabricante() {
		return marcaFabricante;
	}
	public void setMarcaFabricante(String marcaFabricante) {
		this.marcaFabricante = marcaFabricante;
	}
	

}
