package br.univates.samuel.pratica04;

public class Barril {
	public double diametro;
	public double alatura;
	public String material;
	public String bebida;
	public double pressaoAtual;
	public double pressoaMaxima;
	public double temSerpentina;
	public boolean serpentinaAtivada;
	
	public double getDiametro() {
		return diametro;
	}
	public void setDiametro(double diametro) {
		this.diametro = diametro;
	}
	public double getAlatura() {
		return alatura;
	}
	public void setAlatura(double alatura) {
		this.alatura = alatura;
	}
	public String getMaterial() {
		return material;
	}
	public void setMaterial(String material) {
		this.material = material;
	}
	public String getBebida() {
		return bebida;
	}
	public void setBebida(String bebida) {
		this.bebida = bebida;
	}
	public double getPressaoAtual() {
		return pressaoAtual;
	}
	public void setPressaoAtual(double pressaoAtual) {
		this.pressaoAtual = pressaoAtual;
	}
	public double getPressoaMaxima() {
		return pressoaMaxima;
	}
	public void setPressoaMaxima(double pressoaMaxima) {
		this.pressoaMaxima = pressoaMaxima;
	}
	public double getTemSerpentina() {
		return temSerpentina;
	}
	public void setTemSerpentina(double temSerpentina) {
		this.temSerpentina = temSerpentina;
	}
	public boolean isSerpentinaAtivada() {
		return serpentinaAtivada;
	}
	public void setSerpentinaAtivada(boolean serpentinaAtivada) {
		this.serpentinaAtivada = serpentinaAtivada;
	}
	
}
