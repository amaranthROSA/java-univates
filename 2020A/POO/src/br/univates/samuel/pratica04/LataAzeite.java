package br.univates.samuel.pratica04;

public class LataAzeite {
	public String marca;
	public int volume;
	public double ehExtraVirgem;
	public int dataEnvase;
	public int dataValidade;
	
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public int getVolume() {
		return volume;
	}
	public void setVolume(int volume) {
		this.volume = volume;
	}
	public double getEhExtraVirgem() {
		return ehExtraVirgem;
	}
	public void setEhExtraVirgem(double ehExtraVirgem) {
		this.ehExtraVirgem = ehExtraVirgem;
	}
	public int getDataEnvase() {
		return dataEnvase;
	}
	public void setDataEnvase(int dataEnvase) {
		this.dataEnvase = dataEnvase;
	}
	public int getDataValidade() {
		return dataValidade;
	}
	public void setDataValidade(int dataValidade) {
		this.dataValidade = dataValidade;
	}
	

}
