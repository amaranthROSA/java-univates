package br.univates.samuel.pratica04;

public class Estacionamento {
	public int quantidadeAndares;
	public int vagasPorAndar;
	public int tempoParquimetro;
	public double tarifaPorTempo;
	public int tempoMinimo;
	public int tempoMaximo;
	public double multaPorAtraso;
	public double jurosPorAtraso;
	
	public int getQuantidadeAndares() {
		return quantidadeAndares;
	}
	public void setQuantidadeAndares(int quantidadeAndares) {
		this.quantidadeAndares = quantidadeAndares;
	}
	public int getVagasPorAndar() {
		return vagasPorAndar;
	}
	public void setVagasPorAndar(int vagasPorAndar) {
		this.vagasPorAndar = vagasPorAndar;
	}
	public int getTempoParquimetro() {
		return tempoParquimetro;
	}
	public void setTempoParquimetro(int tempoParquimetro) {
		this.tempoParquimetro = tempoParquimetro;
	}
	public double getTarifaPorTempo() {
		return tarifaPorTempo;
	}
	public void setTarifaPorTempo(double tarifaPorTempo) {
		this.tarifaPorTempo = tarifaPorTempo;
	}
	public int getTempoMinimo() {
		return tempoMinimo;
	}
	public void setTempoMinimo(int tempoMinimo) {
		this.tempoMinimo = tempoMinimo;
	}
	public int getTempoMaximo() {
		return tempoMaximo;
	}
	public void setTempoMaximo(int tempoMaximo) {
		this.tempoMaximo = tempoMaximo;
	}
	public double getMultaPorAtraso() {
		return multaPorAtraso;
	}
	public void setMultaPorAtraso(double multaPorAtraso) {
		this.multaPorAtraso = multaPorAtraso;
	}
	public double getJurosPorAtraso() {
		return jurosPorAtraso;
	}
	public void setJurosPorAtraso(double jurosPorAtraso) {
		this.jurosPorAtraso = jurosPorAtraso;
	}
	
	
}
